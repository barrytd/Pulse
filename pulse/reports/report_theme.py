def html_document(report_type: str, doc_title: str, body_html: str) -> str:
    """Wrap section HTML in a full, self-contained light-theme document with
    the brand running-header + footer."""
    head = (
        '<div class="rpt-runhead"><div class="rpt-brandbar">'
        '<div class="rpt-brand"><span class="rpt-brand-mark">' + esc(BRAND) + '</span>'
        '<span class="rpt-brand-sub">' + esc(BRAND_SUB) + '</span></div>'
        '<div class="rpt-eyebrow-r">' + esc(report_type) + '</div></div></div>'
    )
    foot = (
        '<div class="rpt-runfoot"><div class="rpt-footbar">'
        '<span>' + esc(FOOTER_LINE) + '</span>'
        '<span>Confidential</span></div></div>'
    )
    return (
        '<!doctype html><html lang="en"><head><meta charset="utf-8">'
        '<meta name="viewport" content="width=device-width, initial-scale=1">'
        '<title>' + esc(doc_title) + '</title><style>' + RPT_CSS + '</style></head>'
        '<body>' + head + '<div class="rpt-page">' + body_html + '</div>' + foot +
        '</body></html>'
    )


def html_eyebrow_title(eyebrow: str, title: str) -> str:
    return ('<div class="rpt-eyebrow">' + esc(eyebrow) + '</div>'
            '<div class="rpt-title">' + esc(title) + '</div>')


def html_metadata_grid(pairs: Sequence[Tuple[str, str]]) -> str:
    cells = "".join(
        '<div class="k">' + esc(k) + '</div><div class="v">' + (v or html_none()) + '</div>'
        for k, v in pairs
    )
    return '<div class="rpt-meta-grid">' + cells + '</div>'


def html_classification_banner(sev: str, label: Optional[str] = None) -> str:
    k = sev_key(sev)
    txt = label or (("Critical incident" if k == "CRITICAL"
                     else "High-severity incident" if k == "HIGH"
                     else "Moderate findings" if k == "MEDIUM"
                     else "Low-severity findings" if k == "LOW"
                     else "No active findings"))
    return ('<div class="rpt-banner" style="background:' + SEV_FG[k] + ';">'
            'CLASSIFICATION<small>' + esc(txt) + '</small></div>')


def html_section(label: str, inner: str, accent_sev: Optional[str] = None) -> str:
    bar = ' style="background:' + SEV_FG[sev_key(accent_sev)] + ';"' if accent_sev else ""
    return ('<div class="rpt-section"><span class="rpt-section-bar"' + bar + '></span>'
            '<span class="rpt-section-label">' + esc(label) + '</span></div>' + inner)


def html_pill(sev: str) -> str:
    k = sev_key(sev)
    return ('<span class="rpt-pill" style="color:' + SEV_FG[k] + ';background:' + SEV_BG[k] + ';">'
            + esc(k) + '</span>')


def html_status_badge(status: str) -> str:
    return '<span class="rpt-badge">' + esc(STATUS_LABEL.get((status or "new").lower(), "New")) + '</span>'


def html_none() -> str:
    return '<span class="rpt-none">None recorded</span>'


def html_table(headers: Sequence[str], rows: Sequence[Sequence[str]],
               num_cols: Sequence[int] = ()) -> str:
    nset = set(num_cols)
    thead = "".join(
        '<th' + (' class="num"' if i in nset else "") + '>' + esc(h) + '</th>'
        for i, h in enumerate(headers))
    body = ""
    for r in rows:
        tds = "".join(
            '<td' + (' class="num"' if i in nset else "") + '>' + (c if c else "") + '</td>'
            for i, c in enumerate(r))
        body += '<tr>' + tds + '</tr>'
    return ('<table class="rpt-table"><thead><tr>' + thead + '</tr></thead>'
            '<tbody>' + body + '</tbody></table>')


def html_callout(inner: str, accent_sev: Optional[str] = None) -> str:
    style = ' style="border-left-color:' + SEV_FG[sev_key(accent_sev)] + ';"' if accent_sev else ""
    return '<div class="rpt-callout"' + style + '>' + inner + '</div>'


