def pdf_finding_card(num: int, f: dict, styles):
    """A finding card with a severity-colored left border."""
    rl = _rl()
    C = rl["colors"].HexColor
    k = sev_key(f.get("severity"))

    head_bits = ['<font color="%s"><b>#%d</b></font>' % (C_MUTED, num),
                 '<font color="%s"><b>%s</b></font>' % (C_TITLE, esc(f.get("rule") or "Unknown"))]
    head = rl["Paragraph"]("&nbsp;&nbsp;".join(head_bits), styles["cardrule"])

    tags = [pdf_pill_para(k, styles)]
    sub = []
    if f.get("ref_id"):
        sub.append('<font face="Courier" color="%s">%s</font>' % (C_MUTED, esc(f["ref_id"])))
    if f.get("mitre"):
        sub.append('<font color="%s">%s</font>' % (C_ACCENT, esc(f["mitre"])))
    sub.append('<font color="%s">%s</font>' % (C_MUTED, esc(STATUS_LABEL.get((f.get("workflow_status") or "new"), "New"))))
    sub_p = rl["Paragraph"]("&nbsp;·&nbsp;".join(sub), styles["muted"])

    meta_bits = []
    meta_bits.append("<b>When</b> " + esc(f.get("timestamp") or "—"))
    meta_bits.append("<b>Host</b> " + (esc(f["hostname"]) if f.get("hostname") else '<i><font color="%s">None</font></i>' % C_MUTED))
    meta_bits.append("<b>Account</b> " + (esc(f["account"]) if f.get("account") else '<i><font color="%s">None</font></i>' % C_MUTED))
    meta_bits.append("<b>Source IP</b> " + (esc(f["source_ip"]) if f.get("source_ip") else '<i><font color="%s">None</font></i>' % C_MUTED))
    meta = rl["Paragraph"]('<font color="%s" size="8">%s</font>' % (C_MUTED, " &nbsp;·&nbsp; ".join(meta_bits)), styles["muted"])

    desc = rl["Paragraph"](esc(f.get("description") or "No description recorded."), styles["body"])
    sha = rl["Paragraph"]('<font color="%s" face="Courier" size="7">Evidence SHA-256: %s</font>'
                          % (C_FAINT, esc(f.get("sha256") or "—")), styles["mono"])

    inner = rl["Table"]([[head], [tags[0]], [sub_p], [rl["Spacer"](1, 3)], [meta], [desc], [sha]],
                        colWidths=[CONTENT_W - 16])
    inner.setStyle(rl["TableStyle"]([
        ("LEFTPADDING", (0, 0), (-1, -1), 0), ("RIGHTPADDING", (0, 0), (-1, -1), 0),
        ("TOPPADDING", (0, 0), (-1, -1), 1.5), ("BOTTOMPADDING", (0, 0), (-1, -1), 1.5),
    ]))

    card = rl["Table"]([[inner]], colWidths=[CONTENT_W])
    card.setStyle(rl["TableStyle"]([
        ("BOX", (0, 0), (-1, -1), 0.6, C(C_BORDER)),
        ("LINEBEFORE", (0, 0), (0, -1), 4, C(SEV_FG[k])),
        ("TOPPADDING", (0, 0), (-1, -1), 10), ("BOTTOMPADDING", (0, 0), (-1, -1), 10),
        ("LEFTPADDING", (0, 0), (-1, -1), 12), ("RIGHTPADDING", (0, 0), (-1, -1), 12),
    ]))
    return rl["KeepTogether"]([card])


