"""Shared report design system — a light, print-friendly theme that every
Pulse report template (PDF via reportlab + HTML) inherits.

Two parallel surfaces, one look:

  * HTML  — `RPT_CSS` (one self-contained stylesheet) + `html_*` builders.
  * PDF   — `pdf_styles()`, `NumberedCanvas` (brand header + "Page X of Y"
            footer on every page), and `pdf_*` flowable builders.

Both use the same palette and severity colors so a printed PDF and the
in-browser HTML are visually identical. Templates keep their own content
and section order; they just compose these components.

Palette is a light/print adaptation of the dark dashboard tokens (see
.claude/skills/pulse-design.md) — white page, dark-gray body, color
reserved for severity and section accents.
"""
from __future__ import annotations

import html as _html
from typing import Any, List, Optional, Sequence, Tuple

from pulse import __version__ as PULSE_VERSION

