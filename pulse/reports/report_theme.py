def _rl():
    """Import reportlab lazily so HTML/JSON/CSV paths don't pay for it."""
    from reportlab.lib import colors
    from reportlab.lib.enums import TA_LEFT, TA_RIGHT, TA_CENTER
    from reportlab.lib.pagesizes import LETTER
    from reportlab.lib.styles import ParagraphStyle
    from reportlab.lib.units import inch
    from reportlab.pdfgen import canvas as _canvas
    from reportlab.platypus import (
        SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle, Flowable,
        KeepTogether,
    )
    return dict(colors=colors, TA_LEFT=TA_LEFT, TA_RIGHT=TA_RIGHT, TA_CENTER=TA_CENTER,
                LETTER=LETTER, ParagraphStyle=ParagraphStyle, inch=inch, canvas=_canvas,
                SimpleDocTemplate=SimpleDocTemplate, Paragraph=Paragraph, Spacer=Spacer,
                Table=Table, TableStyle=TableStyle, Flowable=Flowable, KeepTogether=KeepTogether)


MARGIN = 50          # ~50px page margins (points)
PAGE_W = 612         # Letter width in points
CONTENT_W = PAGE_W - 2 * MARGIN


def make_numbered_canvas(report_type: str):
    """Return a canvasmaker that stamps the brand header + 'Page X of Y'
    footer on every page (two-pass so the total is known)."""
    rl = _rl()
    Canvas = rl["canvas"].Canvas
    colors = rl["colors"]

    class NumberedCanvas(Canvas):
        def __init__(self, *a, **kw):
            Canvas.__init__(self, *a, **kw)
            self._saved = []

        def showPage(self):
            self._saved.append(dict(self.__dict__))
            self._startPage()

        def save(self):
            total = len(self._saved)
            for state in self._saved:
                self.__dict__.update(state)
                self._draw_furniture(total)
                Canvas.showPage(self)
            Canvas.save(self)

        def _draw_furniture(self, total):
            w, h = self._pagesize
            # ---- header ----
            self.setFillColor(colors.HexColor(C_ACCENT))
            self.setFont("Helvetica-Bold", 11)
            self.drawString(MARGIN, h - 38, BRAND)
            bw = self.stringWidth(BRAND, "Helvetica-Bold", 11)
            self.setFillColor(colors.HexColor(C_MUTED))
            self.setFont("Helvetica", 7)
            self.drawString(MARGIN + bw + 6, h - 38, BRAND_SUB.upper())
            self.drawRightString(w - MARGIN, h - 38, report_type.upper())
            self.setStrokeColor(colors.HexColor(C_RULE))
            self.setLineWidth(1)
            self.line(MARGIN, h - 46, w - MARGIN, h - 46)
            # ---- footer ----
            self.line(MARGIN, 40, w - MARGIN, 40)
            self.setFillColor(colors.HexColor(C_MUTED))
            self.setFont("Helvetica", 7)
            self.drawString(MARGIN, 30, "Page %d of %d" % (self._pageNumber, total))
            self.drawRightString(w - MARGIN, 30, FOOTER_LINE)

    return NumberedCanvas


def pdf_styles():
    rl = _rl()
    PS = rl["ParagraphStyle"]
    C = rl["colors"].HexColor
    return {
        "eyebrow": PS("rpt_eyebrow", fontName="Helvetica-Bold", fontSize=8, leading=11,
                      textColor=C(C_ACCENT), spaceAfter=2),
        "title": PS("rpt_title", fontName="Helvetica-Bold", fontSize=20, leading=24,
                    textColor=C(C_TITLE), spaceAfter=2),
        "body": PS("rpt_body", fontName="Helvetica", fontSize=9.5, leading=14, textColor=C(C_TEXT)),
        "muted": PS("rpt_muted", fontName="Helvetica", fontSize=9, leading=13, textColor=C(C_MUTED)),
        "mono": PS("rpt_mono", fontName="Courier", fontSize=8, leading=11, textColor=C(C_TEXT)),
        "section": PS("rpt_section", fontName="Helvetica-Bold", fontSize=10.5, leading=14,
                      textColor=C(C_TITLE)),
        "verdict": PS("rpt_verdict", fontName="Helvetica-Bold", fontSize=11, leading=16,
                      textColor=C(C_TITLE)),
        "cardrule": PS("rpt_cardrule", fontName="Helvetica-Bold", fontSize=11, leading=14,
                       textColor=C(C_TITLE)),
        "th": PS("rpt_th", fontName="Helvetica-Bold", fontSize=7.5, leading=10, textColor=C(C_MUTED)),
        "td": PS("rpt_td", fontName="Helvetica", fontSize=8.5, leading=12, textColor=C(C_TEXT)),
        "tdmono": PS("rpt_tdmono", fontName="Courier", fontSize=7.5, leading=11, textColor=C(C_TEXT)),
    }


def new_doc(buf, title: str, report_type: str):
    """A configured SimpleDocTemplate + its canvasmaker. Build with
    ``doc.build(story, canvasmaker=canvasmaker)``."""
    rl = _rl()
    doc = rl["SimpleDocTemplate"](
        buf, pagesize=rl["LETTER"],
        leftMargin=MARGIN, rightMargin=MARGIN,
        topMargin=58, bottomMargin=52, title=title,
    )
    return doc, make_numbered_canvas(report_type)


def pdf_section(label: str, styles, accent_sev: Optional[str] = None):
    """Section header: a small colored accent bar + uppercase label."""
    rl = _rl()
    C = rl["colors"].HexColor
    bar = C(SEV_FG[sev_key(accent_sev)]) if accent_sev else C(C_ACCENT)
    cell = rl["Paragraph"](
        '<font color="%s"><b>%s</b></font>' % (C_TITLE, esc(label).upper()), styles["section"])
    t = rl["Table"]([["", cell]], colWidths=[4, CONTENT_W - 4])
    t.setStyle(rl["TableStyle"]([
        ("BACKGROUND", (0, 0), (0, 0), bar),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
        ("LEFTPADDING", (0, 0), (0, 0), 0), ("RIGHTPADDING", (0, 0), (0, 0), 0),
        ("LEFTPADDING", (1, 0), (1, 0), 8),
        ("TOPPADDING", (0, 0), (-1, -1), 2), ("BOTTOMPADDING", (0, 0), (-1, -1), 2),
    ]))
    return t


def pdf_pill_para(sev: str, styles):
    """A severity pill as a one-cell Table flowable (inline use is awkward in
    reportlab, so pills live in their own cell)."""
    rl = _rl()
    C = rl["colors"].HexColor
    k = sev_key(sev)
    p = rl["Paragraph"]('<font color="%s"><b>%s</b></font>' % (SEV_FG[k], esc(k)),
                        rl["ParagraphStyle"]("pill", fontName="Helvetica-Bold", fontSize=7.5,
                                             leading=10, alignment=rl["TA_CENTER"]))
    w = 8 + 5.6 * len(k)
    t = rl["Table"]([[p]], colWidths=[w])
    t.setStyle(rl["TableStyle"]([
        ("BACKGROUND", (0, 0), (-1, -1), C(SEV_BG[k])),
        ("TOPPADDING", (0, 0), (-1, -1), 2), ("BOTTOMPADDING", (0, 0), (-1, -1), 2),
        ("LEFTPADDING", (0, 0), (-1, -1), 5), ("RIGHTPADDING", (0, 0), (-1, -1), 5),
        ("ROUNDEDCORNERS", [5, 5, 5, 5]),
    ]))
    t.hAlign = "LEFT"
    return t


